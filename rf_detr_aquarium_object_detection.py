"""### 4.2 IoU, precision, recall, and classification accuracy (custom implementation)



"""

def compute_iou(box_a, box_b):
    """IoU between two boxes in [x1, y1, x2, y2] format."""
    xa1, ya1, xa2, ya2 = box_a
    xb1, yb1, xb2, yb2 = box_b

    inter_x1, inter_y1 = max(xa1, xb1), max(ya1, yb1)
    inter_x2, inter_y2 = min(xa2, xb2), min(ya2, yb2)
    inter_area = max(0.0, inter_x2 - inter_x1) * max(0.0, inter_y2 - inter_y1)

    area_a = max(0.0, xa2 - xa1) * max(0.0, ya2 - ya1)
    area_b = max(0.0, xb2 - xb1) * max(0.0, yb2 - yb1)
    union = area_a + area_b - inter_area

    return inter_area / union if union > 0 else 0.0


def match_detections(pred_boxes, pred_classes, gt_boxes, gt_classes, iou_threshold=0.5):
    """
    Greedy matching (highest-IoU-first) between predictions and ground truth for ONE image.
    Returns counts of TP, FP, FN, and whether each TP had the correct class (for accuracy).
    """
    matched_gt = set()
    tp, fp, correct_class = 0, 0, 0

    # Sort predictions so the most confident/largest-IoU matches happen first (greedy)
    pairs = []
    for pi, pbox in enumerate(pred_boxes):
        for gi, gbox in enumerate(gt_boxes):
            iou = compute_iou(pbox, gbox)
            if iou >= iou_threshold:
                pairs.append((iou, pi, gi))
    pairs.sort(reverse=True, key=lambda x: x[0])

    matched_pred = set()
    for iou, pi, gi in pairs:
        if pi in matched_pred or gi in matched_gt:
            continue
        matched_pred.add(pi)
        matched_gt.add(gi)
        tp += 1
        if pred_classes[pi] == gt_classes[gi]:
            correct_class += 1

    fp = len(pred_boxes) - len(matched_pred)
    fn = len(gt_boxes) - len(matched_gt)
    return tp, fp, fn, correct_class

