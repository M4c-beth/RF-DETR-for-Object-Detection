"""## 2. RF-DETR Model Implementation

### 2.1 Background: DETR -> RF-DETR

**DETR** (Carion et al., 2020) reframes object detection as direct set prediction:
- A **CNN backbone** extracts spatial feature maps from the image.
- A **Transformer encoder-decoder** attends over these features using a fixed set of learned "object queries," each decoder output corresponding to one predicted object (or "no object").
- A **Hungarian matcher** solves a bipartite matching problem at training time to assign each ground-truth box to exactly one prediction, using a cost that combines classification and box losses. This avoids the need for anchors or non-max suppression.
- **FFN prediction heads** turn each decoder output into a class label and a bounding box (center-x, center-y, width, height).

**RF-DETR** (Robinson, Robicheaux, Popov, Ramanan, Peri — Roboflow, 2025) builds on this with:
- A **DINOv2 vision transformer backbone** instead of a plain CNN, giving stronger, self-supervised pretrained features.
- **Receptive-field enhancement**: multi-scale feature aggregation and a lightweight deformable-attention encoder-decoder (built on Deformable DETR/LW-DETR ideas) so the model can effectively attend to objects at very different scales without the huge compute cost of full global attention — this is the "RF" in RF-DETR, and directly addresses DETR's known weakness on small objects.
- Optimized for a strong **accuracy/latency trade-off**, so the same architecture that gets fine-tuned here can also run in real time.

### 2.2 Where each required component lives in the codebase

| Required component | Where it is in `rfdetr` |
|---|---|
| CNN/backbone | `rfdetr.models.backbone` — DINOv2 ViT backbone |
| Transformer encoder-decoder | `rfdetr.models.transformer` — deformable multi-scale encoder-decoder |
| Receptive Field Enhancement Module | multi-scale feature projection/aggregation layers inside the backbone-to-transformer neck |
| Prediction heads (FFNs) | `rfdetr.models.detr` — classification + bbox regression FFN heads on each decoder layer |
| Hungarian matcher | `rfdetr.models.matcher` — `HungarianMatcher`, used only during training |
| Loss functions | `rfdetr.models.criterion` — classification (focal/CE) loss, L1 box loss, GIoU loss |

"""

